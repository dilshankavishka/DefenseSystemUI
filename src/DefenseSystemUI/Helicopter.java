    private void SliderStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SliderStateChanged
        if(Slider.getValue()<50){ 
            MessageSectionTextArea.setText(MessageSectionTextArea.getText()+"\n"+"Deploy Machine Guns: ");
        }else if(Slider.getValue()<75){ 
            MessageSectionTextArea.setText(MessageSectionTextArea.getText()+"\n"+"Deploy Laser Operation: ");
        }else if(Slider.getValue()<100){ 
            MessageSectionTextArea.setText(MessageSectionTextArea.getText()+"\n"+"Deploy Missile Operation ");
        }else{
            MessageSectionTextArea.setText(MessageSectionTextArea.getText()+"\n"+"Target Out of range ");
        }
    }//GEN-LAST:event_SliderStateChanged
