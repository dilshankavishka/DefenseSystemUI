     @Override
    public void areaStatus(boolean status){
        if(status){
            NotClearedlabel.setText("Area Cleared");
        }else{
            NotClearedlabel.setText("Area Not Cleared");
        }
    }

    @Override
    public void updatePosition(int positionLevel){
        if (CheckBox.isSelected() == true) {
            if (positionLevel > 0) {
                btnShoot.setEnabled(true);
                btnSend.setEnabled(true);
            }else{
                btnShoot.setEnabled(false);
            }
                       
            if (positionLevel > 20) {
                btnShoot.setEnabled(true);
            } else {
                btnShoot.setEnabled(false);
            }
            if (positionLevel > 60) {
                btnLaserOpr.setEnabled(true);
            } else {
                btnLaserOpr.setEnabled(false);
            }
            if (positionLevel > 80) {
                btnMissionOpr.setEnabled(true);
            } else {
                btnMissionOpr.setEnabled(false);
            }
        } else {
            btnShoot.setEnabled(false);
            btnMissionOpr.setEnabled(false);
            btnLaserOpr.setEnabled(false);
            btnSend.setEnabled(false);
        }
        }

    @Override
    public void updateSendMessage(String message, boolean select) {
         if(message != null){   
            if (select == true ) {
                MessageSectionTextArea.setText(MessageSectionTextArea.getText()+"\n"+"Direct Message : \n\t" + message);
            } else if (select == false) {
                MessageSectionTextArea.setText(MessageSectionTextArea.getText()+message);
            }
         }
    }
    
